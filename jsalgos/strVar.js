// You can also assign numbers to variables.
let mass = 200;

// Javascript knows it's a number because it's not wrapped in quotes

console.log(mass + 100);

// Using a variable to create another variable

let originalNum = 30;
let newNum = originalNum + 10;

console.log(newNum);

// Nothing but variables

let oldNum = 40;
let newGenNum = originalNum + oldNum;

console.log(newGenNum);

// changing variable value

oldNum = oldNum + -75;

console.log(oldNum);

// Adding string and number

let testScore = "83";
let newScore = testScore + 7;

console.log(newScore);

console.log(typeof(newScore));
// turns into a string


