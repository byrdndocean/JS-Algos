// Assigning an expression to a variable

let subNum = 33 - 47;

let mulNum = 27 * 3;

let divNum = 48 / 12;

console.log(subNum);
console.log(mulNum);
console.log(divNum);

// We can use variable names and numbers or just variables

console.log(subNum + 10);

console.log(subNum + mulNum);

// modulus operator

let remains = 13 % 8;

console.log(remains);

// Quick operators

// add++
// same as add = add + 1;
// add--
// same as add = add - 1;


//Only original variable gets increased

let num = 1;
let nuNum = num++;

console.log(num);
console.log(nuNum);

// Increment both variables

let numberA = 1;
let numberB = ++numberA;

console.log(numberA);
console.log(numberB);

// Order of operations
// standard order applies


